//强制类型转换：
//自动类型转换时编译器根据代码的上下文环境，自行判断的结果，有时候并不时那么智能，不能满足所有的需求。
//这样就需要强制类型转换就登场：程序员可以再代码中明确提出类型转换
//强制类型需要程序员的干预，提出固定格式的代码来进行类型转换
//强制类型转换的格式：（type_name) expression

#include <stdio.h>

int main(void)
{
	int sum = 103;

	int count = 7;

	double average;

	average = (double) sum / count;

	printf("Average is %1f.\n", average);

	return 0;
}









