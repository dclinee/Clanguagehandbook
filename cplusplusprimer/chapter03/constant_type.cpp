// constant_type.cpp
#include <iostream>
using namespace std;

int main()
{
  // C++ make 2022 constant type store for int type
  cout << "Year = " << 2022 << "\n";
  cout << "sizeof(2022) = " << sizeof(2022) << "\n";

  // C++ make 2022L constant type store for long type
  cout << "Year = " << 2022L << "\n";
  cout << "sizeof(2022L) = " << sizeof(2022L) << "\n";

  // C++ make 2022LL constant type store for long type
  cout << "Year = " << 2022LL << "\n";
  cout << "sizeof(2022LL) = " << sizeof(2022LL) << "\n";

  return 0;
}
