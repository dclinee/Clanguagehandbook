// endl_newlin.cpp -- display endl and '\n'
#include <iostream>
using namespace std;

int main()
{
  // long string with '\n'
  cout << endl << endl << "What next?" << endl << "Enter a number:" << endl;
  cout << "\n\nWhat next?\nEnter a number:\n";

  // display number with endl
  cout << 2022 << endl;

  // the end of string with '\n'
  cout << "hello world!\n";


  return 0;
}
