// compound_statement.c
#include <stdio.h>
int main(void)
{
  int toes = 12; // assignment statement
  printf("%d\n", toes); // function expression statement
  ; // blank statement
  // compound statement
  int years = 0;
  while((years) < 100)
    {
      int wisdom = wisdom * 1.05;
      printf("%d %d\n", years, wisdom);
      years = years + 1;
    }
  return 0;
}
